void main(){
  print('Logika AND');
  print('true && true\t: ${true && true}');
  print('true && false\t: ${true && false}');
  print('false && true\t: ${false && true}');
  print('false && false\t: ${false && false}');

  print('Logika OR');
  print('true || true\t: ${true || true}');
  print('true || false\t: ${true || false}');
  print('false || true\t: ${false || true}');
  print('false || false\t: ${false || false}');

  
  
}